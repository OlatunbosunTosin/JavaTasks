public class NumberPattern{
    public static void main(String[] args){
        for(int firstColumn = 0; firstColumn <= 9; firstColumn++){
            for(int secondColumn = 0; secondColumn <= 9; secondColumn++){
                for(int thirdColumn = 0; thirdColumn <= 9; thirdColumn++){
                    for(int fourthColumn = 0; fourthColumn <= 9; fourthColumn++){
                System.out.println(" "+ firstColumn + secondColumn + thirdColumn + fourthColumn);
            }
        }
}}
}
}
